    private void userSave() {
        if(fullName.getText().trim().isEmpty() && companyName.getText().trim().isEmpty()&& phone.getText().trim().isEmpty() && mail.getText().trim().isEmpty()
                && address.getText().trim().isEmpty() && nic.getText().trim().isEmpty()){
            
            l1.setText("Required Name");
            l2.setText("Required Company Name");
            l3.setText("Required Phone No");
            l4.setText("Required Email");
            l5.setText("Required Address");
            l6.setText("Required NIC No");
        }
        else if(fullName.getText().trim().isEmpty()){
            l1.setText("Required Name");
        }
        else if(companyName.getText().trim().isEmpty()){
            l2.setText("Required Company Name");
        }
        else if(phone.getText().trim().isEmpty()){
            l3.setText("Required Phone No");
        }
        else if(mail.getText().trim().isEmpty()){
            l4.setText("Required Email");
        }
        else if(address.getText().trim().isEmpty()){
            l5.setText("Required Address");
        }
        else if(nic.getText().trim().isEmpty()){
            l6.setText("Required NIC No");
        }
        
        else{
            
            
            user go = new user();                     // Object Concept
             
            go.setuserID(id.getText());
            id.setText("");
            
            go.setname(fullName.getText());
            fullName.setText("");
            
            go.setcName(companyName.getText());
            companyName.setText("");
            
            go.setphoneNo(phone.getText());
            phone.setText("");
            
            go.setemail(mail.getText());
            mail.setText("");
            
            go.setuserAddress(address.getText());
            address.setText("");
            
            go.setnicNo(nic.getText());
            nic.setText("");
            
            
            go.addUser();
            
            autoID();
            
            fullName.setText("");
            companyName.setText("");
            phone.setText("");
            mail.setText("");
            address.setText("");
            nic.setText("");
            
            
        }
    }


