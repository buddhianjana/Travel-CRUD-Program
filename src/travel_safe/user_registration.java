     public void autoID()
    {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/travelsafe","root","");
            Statement s = con.createStatement();

            ResultSet rs = s.executeQuery("select Max(userID) from user");
            rs.next();

            rs.getString("Max(userID)");


            if(rs.getString("Max(userID)") == null)
            {
                
                id.setText("U-001");
            }
            
            else
                
            {
                Long userID = Long.parseLong(rs.getString("Max(userID)").substring(2,rs.getString("Max(userID)").length()));
                userID++;
                id.setText(String.format("U-"+"%03d", userID)); 
                
            }


        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(autoIn.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

