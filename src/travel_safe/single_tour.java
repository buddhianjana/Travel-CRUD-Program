    private void jLabel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MouseClicked
       
        l1.setText("");
        
        if(sDate.getDate()== null){
        l1.setText("Required Date");
        
        }
        
        else if(sDate.getDate()== null){
            l1.setText("Required Date");
        }
       
        else{    
        try {
                // TODO add your handling code here:
                String tourID = tourNo.getText();
                String userID = (String) id.getSelectedItem();
                SimpleDateFormat dat = new SimpleDateFormat ("yyyy-MM-dd");
                String date = dat.format (sDate.getDate());
                String location = (String) loca.getSelectedItem();
                String vehicletype = (String) veh.getSelectedItem();
                String duration = (String) time.getSelectedItem();
                String passengers = (String) pass.getSelectedItem();
                tourNo.requestFocus();

                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/travelsafe","root","");
                PreparedStatement pst = con.prepareStatement("insert into tour (tourID,userID,date,location,vehtype,duration,passenger)values(?,?,?,?,?,?,?)");
                
                pst.setString(1, tourID);
                pst.setString(2, userID);
                pst.setString(3, date);
                pst.setString(4, location);
                pst.setString(5, vehicletype);
                pst.setString(6, duration);
                pst.setString(7, passengers);
                pst.executeUpdate();

                JOptionPane.showMessageDialog(this,"Tour Added Successfully.");

                autoID();
                
                id.setSelectedItem("");
                sDate.setDate(null);
                loca.setSelectedItem("");
                veh.setSelectedItem("");
                time.setSelectedItem("");
                pass.setSelectedItem(""); 


               }catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(autoIn.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jLabel7MouseClicked

