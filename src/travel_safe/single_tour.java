     public final void autoID() {
        
         try {
            
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/travelsafe","root","");
            Statement s = con.createStatement();

            ResultSet rs = s.executeQuery("select Max(tourID) from tour");
            rs.next();

            rs.getString("Max(tourID)");


            if(rs.getString("Max(tourID)") == null)
            {

                tourNo.setText("T-001");
            }
            else
            {
                Long tourID = Long.parseLong(rs.getString("Max(tourID)").substring(2,rs.getString("Max(tourID)").length()));
                tourID++;
                tourNo.setText(String.format("T-"+"%03d", tourID));

            }


        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(autoIn.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

